#include "libft.h"

int		ft_isalpha(char *s)
{
	int i;

	i = 0;
	while ((s[i] >= 'a' && s[i] <= 'z') || (s[i] >= 'A' && s[i] <= 'Z'))
		i++;
	if (s[i] != '\0')
		return (0);
	return (1);
}
